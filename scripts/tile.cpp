Tile::Tile() {
    tile1 = TextureLoader::loadTexture("resources/img/tile32_light.png");
    tile2 = TextureLoader::loadTexture("resources/img/tile32_dark.png");
    loadTiles();
    src.x = src.y = 0;
    src.w = dist.w = src.h = dist.h = 32;
}

Tile::~Tile() {

}

void Tile::loadTiles() {
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 30; j++) {
            tiles[i][j] = arr[i][j];
        }
    }

}


void Tile::drawTile() {
    int color;
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 30; j++) {
            color = tiles[i][j];
            dist.x = j * 32;
            dist.y = i * 32;
            if (color == 0) {
                TextureLoader::Draw(tile1, src, dist);
            } else {
                TextureLoader::Draw(tile2, src, dist);
            }

        }
    }
}



